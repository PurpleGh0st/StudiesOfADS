let primeiroNumero = Number(prompt("Digite um número:"));
let segundoNumero = Number(prompt("Digite outro número:"));

let resultadoAd = primeiroNumero + segundoNumero;
let resultadoSub = primeiroNumero - segundoNumero;
let resultadoMul = primeiroNumero * segundoNumero;
let resultadoDiv = primeiroNumero / segundoNumero;

console.log("Adição: " + resultadoAd);
console.log("Subtração: " + resultadoSub);
console.log("Multiplicação: " + resultadoMul);
console.log("Divisão: " + resultadoDiv);